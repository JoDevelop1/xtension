function toChromeMessages(messages) {
  return Object.fromEntries(
    Object.entries(messages).map(([key, message]) => [key, { message }])
  );
}

function writeLocales(targetDir) {
  const localesDir = path.join(targetDir, "_locales");
  fs.rmSync(localesDir, { force: true, recursive: true });

  for (const [locale, messages] of Object.entries(localeMessages).filter(([locale]) => supportedLocales.has(locale))) {
    const localeDir = path.join(localesDir, locale);
    fs.mkdirSync(localeDir, { recursive: true });
    fs.writeFileSync(
      path.join(localeDir, "messages.json"),
      `${JSON.stringify(toChromeMessages(messages), null, 2)}\n`,
      "utf8"
    );
  }
}

